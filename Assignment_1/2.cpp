// Take length and breadth as input (both integers) and calculate the area of the rectangle.
#include<iostream>
using namespace std;
int main(){
    int length, breadth;
    cout << "Input Of Length:"<<endl;
    cin >> length;
    cout << "Input Of Breadth"<<endl;
    cin >> breadth;
    cout << "Area Of Rectangle: " << length + breadth <<endl;
    return 0;
}
